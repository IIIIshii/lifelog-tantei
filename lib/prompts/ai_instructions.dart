// Geminiへ渡すシステム指示・隠し指示を一元管理するファイル。
// キャラクター（人格・固定質問）の定義は lib/roles/ に移管済み。
// ここはロール非依存の指示（日記生成・解析・フォローアップの定型部）を扱う。
class AiInstructions {
  AiInstructions._();

  // ── AIフォローアップ質問時の隠し指示 ─────────────────────────
  // generateFollowUp 呼び出し時にプロンプトへ追加する。UI には表示しない。
  // モデルには responseSchema により {sufficient, question} のJSONを返させる。
  //
  // interviewerInstruction には選択中ロールの人格指示（Role.interviewerInstruction）を渡す。
  // 人格は呼び出し側モデルの systemInstruction にも入っているため、ここでの注入は1回に留める。
  static String followUpHint(String interviewerInstruction) {
    return '$interviewerInstruction\n'
      'この人格のまま、この証言を読み、まだ見えていない場面・背景・感情の余白を一つ選んで質問してください。\n'
      'トピックをチェックリスト的に埋めるのではなく、話の流れから自然に浮かび上がる\n'
      '「その場の空気感」や「そのとき何を感じていたか」を掘り下げるような問いかけにしてください。\n'
      '探偵らしく短く、しかし的を射た質問を一つだけ投げてください。\n'
      '\n'
      '出力は次のJSON形式に従ってください：\n'
      '- 証言が十分に語られていると判断した場合: {"sufficient": true, "question": ""}\n'
      '- まだ深掘りすべきと判断した場合: {"sufficient": false, "question": "次の質問文"}\n'
      'question 以外の余計な文章や前置きは出力しないでください。';
  }

  // ── 自由記述へのリアクション時の隠し指示 ─────────────────────
  // generateReaction 呼び出し時にプロンプトへ追加する。質問ではなく、直近の証言への
  // 短い相槌・気づきを1文だけ返させる（次の固定質問は別途投げられるため問いかけない）。
  //
  // interviewerInstruction には選択中ロールの人格指示（Role.interviewerInstruction）を渡す。
  // 人格は呼び出し側モデルの systemInstruction にも入っているため、ここでの注入は1回に留める。
  static String reactionHint(String interviewerInstruction) {
    return '$interviewerInstruction\n'
        'この人格のまま、直前の依頼人の証言に対して短い相槌・気づきのコメントを返してください。\n'
        '【厳守】\n'
        '- 質問はしないこと。問いかけや「？」で終わらせない（次の質問は別途投げられる）。\n'
        '- 1文だけ、簡潔に。前置き・見出し・引用符・依頼人の発言の繰り返しは不要。\n'
        '- ネガティブな言葉・評価は一切使わないこと。\n'
        '- 本文（コメント文）のみを出力すること。';
  }

  // ── 日記生成モデルのシステム指示 ─────────────────────────────
  // 日記生成専用 GenerativeModel の systemInstruction として事前にセットする。
  // 文体・視点（口調・人称・記録の形式）は選択中ロールの Role.diaryStyle を差し込み、
  // ここでは字数・ネガティブ禁止・本文のみといったロール非依存の不変ルールだけを規定する。
  // 三人称は強制しない（ロールにより一人称・二人称もあり得る）。
  static String diaryWriter(String diaryStyle) {
    return '上記の証言をもとに、次の文体で今日の記録を100〜300字で生成してください。\n\n'
        '【文体・視点】\n'
        '$diaryStyle\n\n'
        '【ルール】\n'
        '- 指定された文体・視点を保って記述する\n'
        '- ネガティブな表現・評価は一切含めない\n'
        '- 証言にある事実をもとに記録する（事実にない事柄を創作しない）\n'
        '- 日付や「捜査番号」などの見出しは不要。本文のみ出力する';
  }

  // ── 解析モデルのシステム指示 ─────────────────────────────────
  // 直近14日分の事件簿（日記＋回答）を読み返し、所見を記す役。
  // 文体は選択中ロールの Role.analystStyle を差し込み、役割とルールはここに集約する。
  static String analyst(String analystStyle) {
    return 'あなたは過去の事件簿群を読み返し、傾向と兆候を所見として記す役です。\n'
        '依頼人の直近の記録（日記と回答）を俯瞰し、繰り返されているパターン・気になる兆候・'
        '取り組みの中で光っている点を、次の文体で簡潔に記録してください。\n\n'
        '【文体・視点】\n'
        '$analystStyle\n\n'
        '【ルール】\n'
        '- 指定された文体・視点を保って記述する\n'
        '- ネガティブな断定や評価（怠けている、ダメ、など）は使わない\n'
        '- データから読み取れない事柄を断定しない（推測は推測として記す）\n'
        '- 数字や固有名詞は記録から拾って具体的に書く';
  }
}
