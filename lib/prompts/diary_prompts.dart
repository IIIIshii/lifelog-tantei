// Geminiへ投げるプロンプト文字列を一元管理するクラス
// 探偵のキャラクター・口調・ルールをここで一元管理する。
// ロール変更や文体調整はこのファイルだけ触ればOK。
class DiaryPrompts {
  DiaryPrompts._(); // インスタンス化禁止

  // ── キャラクター定義 ────────────────────────────────────────
  // 全プロンプトに共通して使用するハードボイルド探偵のペルソナ
  static const String _detectivePersona =
      'あなたは寡黙でクールなハードボイルド探偵です。\n'
      '依頼人の証言を聞き、今日の出来事の全貌を把握するのが仕事です。\n'
      'ハードボイルドな探偵の口調で話してください。\n'
      '相槌や気づきのコメントを添えても構いません。\n'
      'ネガティブな言葉・評価は一切使わないこと。\n';

  // ── セッション開幕セリフ ────────────────────────────────────
  // 固定文字列のためAPIを呼ばない。変更したい場合はここだけ修正する。
  static String get openingLine =>
      '新しい依頼が来たようだ。今日の出来事を話してくれ。';

  // ── 深堀り質問プロンプト ────────────────────────────────────
  // AIが証言の「余白」を読み取り、まだ見えていない場面や感情を引き出す質問をする。
  // トピックを機械的に埋めるのではなく、話の輪郭を自然に膨らませていくイメージ。
  // 十分な情報が揃ったと判断した場合は「DONE」とだけ返す（バックエンドで検知）。
  static String followUp(String conversationHistory) {
    return '$_detectivePersona\n'
        '【これまでの証言】\n'
        '$conversationHistory\n\n'
        '【指示】\n'
        'この証言を読み、まだ見えていない場面・背景・感情の余白を一つ選んで質問してください。\n'
        'トピックをチェックリスト的に埋めるのではなく、話の流れから自然に浮かび上がる\n'
        '「その場の空気感」や「そのとき何を感じていたか」を掘り下げるような問いかけにしてください。\n'
        '探偵らしく短く、しかし的を射た質問を一つだけ投げてください。\n'
        '証言が十分に語られていると判断した場合のみ「DONE」とだけ返してください。';
  }

  // ── 捜査ログ（日記）生成プロンプト ──────────────────────────
  // 会話全体を受けて、探偵視点の三人称捜査ログを100〜300字で生成する。
  // ネガティブ表現禁止・事実記録スタイルを厳守する。
  static String generateDiary(String conversationHistory) {
    return '$_detectivePersona\n'
        '【これまでの証言】\n'
        '$conversationHistory\n\n'
        '【指示】\n'
        '上記の証言をもとに、探偵が記録した三人称の捜査ログを100〜300字で生成してください。\n\n'
        '【ルール】\n'
        '- 「依頼人は〜」「〜が確認された」「〜との証言を得た」などの三人称で記述する\n'
        '- ハードボイルドで簡潔な文体を使う\n'
        '- ネガティブな表現・評価は一切含めない\n'
        '- 事実を淡々と記録するスタイルにする\n'
        '- 日付や「捜査番号」などの見出しは不要。本文のみ出力する';
  }
}
